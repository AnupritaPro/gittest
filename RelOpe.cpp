//Relational Operator

#include<iostream>
using namespace std;
int main(){

	int a=20,b=20;
	

	if(a==b	)			//equal
	cout<<"a is equal to b"<<endl;

	else if(a<b)			//less than operator
	cout<<"a is not less than b"<<endl;

	else if (a>b)			//greater than operator
	cout<<"a is greater than b"<<endl;

	else if (a!=b)			//not equal
	cout<<"a is not equal to b"<<endl;
}
