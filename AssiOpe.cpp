//Assigment Operator

#include<iostream>
using namespace std;
int main(){
	int b=2,c=4;        
	c+=b;                 //Adding
	cout<<"Addition of number is:"<<c<<endl;
	
	c-=b;			//Subtracting
	cout<<"subtraction of number is:"<<c<<endl;

	c*=b;			//Multiplication
	cout<<"multiplication of number is "<<c<<endl;

	c/=b;			//Division
	cout<<"Division of number "<<c<<endl;

	c%=b;			//Module
	cout<<"module of number is "<<c<<endl;

}

