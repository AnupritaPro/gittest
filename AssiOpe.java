//Assigment Operator

class AssiOpe{
public static void main(String args[]){
	int a=24,b=2;

	a+=b;				//a=a+b
	System.out.println("Addition of number:\t"+a);

	a-=b;				//a=a-b
	System.out.println("Subtraction of number\t"+a);

	a*=b;				//a=a*b
	System.out.println("Multiplication of number:\t"+a);

	a/=b;				//a=a/b
	System.out.println("Division of number:\t"+a);

	a%=b;				//a=a%b
	System.out.println("Mode of number:\t"+a);

	

	}
}
